	/**
	 * The doGet method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		studentlog(request, response);
	}

	private void studentlog(HttpServletRequest request,
			HttpServletResponse response) throws ServletException, IOException {
		String username = request.getParameter("n1");// 获取用户名
		String password = request.getParameter("m1");// 获取密码
		manager manage = new manager();
		manage.setUsername(username);
		manage.setPassword(password);
		boolean log = managerdao.managerlogin(manage);
		if (log) {
			request.getRequestDispatcher("/backstageframe.jsp").forward(request,
					response);

		} else {
			
			response.sendRedirect("/xuanke/managerLogin.jsp?error=yes");
		}
	}

